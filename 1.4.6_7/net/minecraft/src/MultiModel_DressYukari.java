	@Override
	public void setLivingAnimationsLM(EntityLiving entityliving, float f, float f1, float f2) {
		super.setLivingAnimationsLM(entityliving, f, f1, f2);
		IdOffset=entityliving.entityId;
		if (getCapsValueBoolean(caps_isLookSuger))
			pink.setVisible(true);
		else
			pink.setVisible(false);
	}

	@Override
	public void setRotationAnglesLM(float f, float f1, float f2, float f3, float f4, float f5, Entity entity) {
		super.setRotationAnglesLM(f, f1, f2, f3, f4, f5, entity);

		float t;
		if(isRiding
				&& entity.ridingEntity != null)
			t=entity.ridingEntity.ticksExisted;
		else
			t=f2;

		Ahoge.rotateAngleY=MathHelper.cos(t * 0.2F + MathHelper.cos(t*0.05F+IdOffset) * 1.0F) * 0.2F;
		Ahoge.rotateAngleX=2.9F;

		Skirt.rotateAngleX = 0f;
		Skirt.rotationPointY -= 9F;
		Skirt.rotationPointZ=0f;

		if(isRiding) {
			Skirt.rotationPointY-=0.2f;
			Skirt.rotationPointZ+=1.0f;
			Skirt.rotateAngleX = getCapsValueFloat(caps_convertDegtoRad, -25F);
		}
	}

